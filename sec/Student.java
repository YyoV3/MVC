package sec;

public class Student {
	private String ID;
	private String Name;
	private String Mark;
	
	
	 public void setID(String ID) {
	      this.ID = ID;
	   }
	 public void setName(String Name) {
	      this.Name = Name;
	   }
	 public void setMark(String Mark) {
	      this.Mark = Mark;
	   }
	
	
	
	 public String getID() {
	      return ID;
	   }
	 public String getName() {
	      return Name;
	   }
	 public String getMark() {
	      return Mark;
	   }

}
